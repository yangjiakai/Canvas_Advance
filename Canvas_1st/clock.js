let WINDOW_WIDTH = 700
let WINDOW_HEIGHT = 1400
let RADIUS = 8

window.onload = function () {
  //------------------------------
  // init
  //------------------------------
  let canvas = document.querySelector('#canvas')
  canvas.height = WINDOW_WIDTH
  canvas.width = WINDOW_HEIGHT
  let context = canvas.getContext('2d')

  render(context)
}

//------------------------------
// draw
//------------------------------
function draw (part,ctx){
  ctx.beginPath()
  ctx.moveTo(part.point1.x,part.point1.y)
  ctx.lineTo(part.point2.x,part.point2.y)
  ctx.lineTo(part.point3.x,part.point3.y)
  ctx.closePath()
  ctx.fillStyle = part.color
  ctx.fill();
  ctx.strokeStyle = '#000'
  ctx.lineWidth = 3
  ctx.stroke()
}

//------------------------------
// render
//------------------------------
function render(cxt){
  let hours = 12
  let minutes = 22
  let seconds = 32
  renderDigit(0,0,parseInt(hours/10),cxt)
}

//------------------------------
// renderDigit
//------------------------------
function renderDigit(x,y,num,cxt){
  cxt.fillStyle = '#4D5C99'
  for (let i = 0; i < digit[num].length; i++) {
   for (let j = 0; j < digit[num][i].length; j++) {
     if (digit[num][i][j] == 1) {
        cxt.beginPath()
        cxt.arc(x+j*2*(RADIUS+1)+(RADIUS+1),y+i*2*(RADIUS+1)+(RADIUS+1),RADIUS,0,2*Math.PI)
        cxt.closePath()
        cxt.fill()
     }
     
   }
    
  }
}


